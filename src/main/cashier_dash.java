    private void kButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton2ActionPerformed
        String productName = NameOfProduct.getText();
        DecimalFormat jk = new DecimalFormat("###,###.##");
        try {
            NumberFormat usFormat = NumberFormat.getNumberInstance(java.util.Locale.US);
            double amount = usFormat.parse(Amount.getText()).doubleValue();
            double quantity = usFormat.parse(Quantity.getText()).doubleValue();
            double discount = usFormat.parse(Discount.getText()).doubleValue();
            double amountRecieved = usFormat.parse(AmountRecieved.getText()).doubleValue();

            //Amount*Quantity-Discount
            double ADQ = amount * quantity - discount;
            //AMR = AmountRecieved - ADQ
            double AMR = amountRecieved - ADQ;

            String x = jk.format(AMR);
            Change.setText("" + x + ".00");

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_kButton2ActionPerformed

