        String amount = Amount.getText();
        String discount = Discount.getText();
        String quantity = Quantity.getText();
        java.sql.Date date = new java.sql.Date(Date.getDate().getTime());
        String amountrecieved = AmountRecieved.getText();
        String change = Change.getText();
        String NetAmount = net.getText();
