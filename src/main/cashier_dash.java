    private void QuantityKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_QuantityKeyReleased
        try {
            DecimalFormat jk = new DecimalFormat("###,### - PCS");
            NumberFormat usFormat = NumberFormat.getNumberInstance(java.util.Locale.US);
            double quan = usFormat.parse(Quantity.getText()).doubleValue();
            double AvQuant = usFormat.parse(AvQuantity.getText()).doubleValue();
            double TotalQua = AvQuant - quan;
            String x = jk.format(TotalQua);
            AvQuantity1.setText("" + x);
        } catch (ParseException ex) {
            Logger.getLogger(cashier_dash.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_QuantityKeyReleased

